const sampleEmojis = ['‚ù§Ô∏è', 'üòä', 'ü•∞', 'üòç', 'üòò', 'üíï', '‚ú®', 'üåü', 'üíñ', 'üòÑ', 'üôÇ', 'üòÅ', 'üéâ', 'üéä', 'üíØ', 'üî•', 'üëç', 'üëè', 'üôå', 'üí™']

function getRandomElement<T>(array: T[]): T {
  return array[Math.floor(Math.random() * array.length)]
}

function getRandomInt(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1)) + min
}

export function generateSampleData() {
  const now = new Date()
  const sixMonthsAgo = new Date(now)
  sixMonthsAgo.setMonth(sixMonthsAgo.getMonth() - 6)
  
  const messages: Array<{
    date: Date
    text: string
    isFromMe: boolean
    wordCount: number
    hour: number
    dayOfWeek: number
    dayName: string
    weekKey: string
    monthKey: string
    sentiment: number
  }> = []
  
  // Generate ~500 messages over the past 6 months
  const numMessages = 500
  const timeRange = now.getTime() - sixMonthsAgo.getTime()
  
  for (let i = 0; i < numMessages; i++) {
    // Random time within the past 6 months
    const randomTime = sixMonthsAgo.getTime() + Math.random() * timeRange
    const date = new Date(randomTime)
    
    // Alternate between you and them, but with some randomness
    const isFromMe = Math.random() > 0.45 // Slightly more messages from "them"
    
    // Get a random message, sometimes add emoji
    let text = getRandomElement(sampleMessages)
    if (Math.random() > 0.7) {
      text += ' ' + getRandomElement(sampleEmojis)
    }
    
    const hour = date.getHours()
    const dayOfWeek = date.getDay()
    const dayNames = ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday']
    const dayName = dayNames[dayOfWeek]
    
    const wordCount = text.match(/\b\w+\b/g)?.length || 0
    
    // Week key
    const year = date.getFullYear()
    const startOfYear = new Date(year, 0, 1)
    const days = Math.floor((date.getTime() - startOfYear.getTime()) / (24 * 60 * 60 * 1000))
    const week = Math.floor(days / 7)
    const weekKey = `${year}-W${String(week).padStart(2, '0')}`
    
    const monthKey = `${year}-${String(date.getMonth() + 1).padStart(2, '0')}`
    
    // Simple sentiment (slightly positive on average)
    const sentiment = (Math.random() - 0.3) * 0.6 // Range roughly -0.3 to 0.3, slightly positive
    
    messages.push({
      date,
      text,
      isFromMe,
      wordCount,
      hour,
      dayOfWeek,
      dayName,
      weekKey,
      monthKey,
      sentiment
    })
  }
  
  // Sort by date
  messages.sort((a, b) => a.date.getTime() - b.date.getTime())
  
  return messages
}

